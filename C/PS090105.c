/*
	
PS090105
 
設計一個程式，該程式可以連續讀入正整數(輸入0或輸入20個整數後，會結束輸入)，
之後讀入一個整數N，找出剛剛第N個輸入的正整數(N從0起算)，
如果N大於所輸入的個數，則顯示NO。

=== 範例執行結果(1) ===
1 3 5 0 <- 輸入
1 <- 輸入
3

=== 範例執行結果(2) ===
55 44 33 22 11 0 <- 輸入
5 <- 輸入
NO
*/
#include<stdio.h>

int main(void)
{
	int i,a[80],n;
	for(i=0;;i++)
	{
		scanf("%d ",&a[i]);
		if(a[i]==0 || a[i]==20)
		{
			break;
		}
	}
	scanf("%d",&n);
	
	if(n>i)
	{
		printf("NO");
	}
	else
	{
		printf("%d",a[n]);
	}
	
	return 0;
}
