/*
	
PS090103
 
設計一個程式，該程式會讀入一個整數數列(輸入0表示結束，數列至多包含80個整數)，之後再讀入一個整數，程式可以找出該整數出現在數列中的次數。

=== 範例執行結果(1) ===
1 3 5 3 1 0 <- 輸入數列
3 <- 輸入整數
2

=== 範例執行結果(2) ===
55 66 55 66 0 <- 輸入數列
55 <- 輸入整數
2
*/
#include<stdio.h>

int main(void)
{
	int i,n,s[80],end,a=0,k;
	
	for(i=0;i<80;i++)
	{
		scanf("%d",&s[i]);
		if(s[i]==0)
		{
			break;	
		}	
	}
	end=i;
	scanf("%d",&n);
	for(k=0;k<end;k++)
	{
		if(n==s[k])
		{
			a++;	
		}	
	}	
	printf("%d",a);
	return 0;
}
